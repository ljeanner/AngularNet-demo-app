@Injectable()
export class WeatherService {
  constructor() {}

  // Helper function to convert Fahrenheit to Celsius
  private fahrenheitToCelsius(fahrenheit: number): number {
      const FAHRENHEIT_FREEZING_POINT = 32;
      const FAHRENHEIT_TO_CELSIUS_RATIO = 5 / 9;
      return (fahrenheit - FAHRENHEIT_FREEZING_POINT) * FAHRENHEIT_TO_CELSIUS_RATIO;
  }
  
  // Helper function to calculate the sum of an array
  private calculateSum(numbers: number[]): number {
      return numbers.reduce((acc, curr) => acc + curr, 0);
  }
  
  // Main function to calculate the average temperature in Celsius
  /**
   * Calculates the average temperature from an array of temperatures.
   * 
   * @param temps - An array of temperatures.
   * @returns The average temperature in Celsius.
   */
  public avgTemp(temps: number[]): number {
      const sum = this.calculateSum(temps);
      const avg = sum / temps.length;
      return this.fahrenheitToCelsius(avg);
  }
}
